package com.zjwy.tiaobaojinew.bean;

public class BrandBean {
	private String id;
	private String name;
	private String name_chinese;
	private String code;
	private String thumb;
	private String country;
	private String decade_start;
	private String picture_story;
	private String listorder;
	private String description;
	private String num;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName_chinese() {
		return name_chinese;
	}

	public void setName_chinese(String name_chinese) {
		this.name_chinese = name_chinese;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getThumb() {
		return thumb;
	}

	public void setThumb(String thumb) {
		this.thumb = thumb;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getDecade_start() {
		return decade_start;
	}

	public void setDecade_start(String decade_start) {
		this.decade_start = decade_start;
	}

	public String getPicture_story() {
		return picture_story;
	}

	public void setPicture_story(String picture_story) {
		this.picture_story = picture_story;
	}

	public String getListorder() {
		return listorder;
	}

	public void setListorder(String listorder) {
		this.listorder = listorder;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}
}
